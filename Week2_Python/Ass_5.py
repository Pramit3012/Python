import math
sin_60_degrees = math.sin(math.radians(60))

cos_pi = math.cos(math.pi)
sin_value = math.sin(0.8660254037844386)
tan_90_degrees = math.tan(math.radians(90))

print("sin of 60 degrees is:", sin_60_degrees)
print("cos of pi is:", cos_pi)
print("sin(0.8660254037844386) is:", sin_value)
print("tan of 90 degrees is:", tan_90_degrees)
